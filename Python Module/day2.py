# Day 2 Practice - Learning Python Basics

# Variables
name = "Pavan"
age = 23
learning = "Agentic AI"

print("Name:", name)
print("Age:", age)
print("Learning:", learning)

# Multiple Assignment
city, state = "Hyderabad", "Telangana"

print("\nCity:", city)
print("State:", state)

# Swapping Variables
a = 10
b = 20

print("\nBefore Swapping")
print("a =", a)
print("b =", b)

a, b = b, a

print("\nAfter Swapping")
print("a =", a)
print("b =", b)

# Type Conversion
marks = 95

print("\nOriginal Type:", type(marks))

marks_float = float(marks)
print("Float Value:", marks_float)

marks_bool = bool(marks)
print("Boolean Value:", marks_bool)

